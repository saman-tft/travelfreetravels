<script>
$(document).ready(function() {
	$('.theme-toggle-list li a').click(function(e) {
		e.preventDefault();
		 var skin_class_re = new RegExp('skin[a-zA-Z-]+');
		var skin_name = skin_class_re.exec($('body').attr('class'));
		if (skin_name != null) {
			//remove
			$('body').removeClass(skin_name[0]);
		}
		$('body').addClass($(this).data('skin'));
	});
});
</script>